# Ввод длины ребра куба
a = int(input("Введите длину ребра куба: "))

# Вычисление объема куба
volume = a ** 3

# Вычисление площади полной поверхности куба
surface_area = 6 * (a ** 2)

# Вывод результатов
print("Объем куба равен:", volume)
print("Площадь полной поверхности куба равна:", surface_area)